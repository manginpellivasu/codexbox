package Assesment;

public class SwithOperators {
    public static void main(String[] args) {
        int a = 10, b = 20;
        char c='*';
        switch (c) {
            case '+':
                System.out.println("addition:" + (a + b));
                break;
            case '-':
                System.out.println("substaction:"+(a-b));
                break;
            case '*':
                System.out.println("multiplication:"+(a*b));
                break;
            case '/':
                System.out.println("division:"+(a/b));

        }
    }
}





